   <h4>VYHODNOCENÍ</h4>
   <p>Tyhle všechny kriteria padají, když hráč vydělá týdně 8*60=480prestiže. Takové množství prestiže je potřeba z turnajů aby s prestiží z KM, jsem mohl kupovat 2bf na den.
   <p>Naopak pokud mi hráč neoznámí, že nemůže hrát, tak ho klidně vyhodím jen co mu zašedne puntík v seznamu hráčů. Pokud ho nevyhodím závisí, jestli byl v klubu aktivní, dobrý hráč, podle soupeřů v KM, jestli máme dost prestiže a jak se vyspím.
   <p>Pořád platí 3stupně při řešení problému s hráčem. Upozornění, varování a vyhazov. Každý týden jeden stupeň.
   Pokud hráč nehraje dostatečný počet turnajů(7 týdně) a nezíská přes 480prestiže, tak jej před nakupováním benefitu vyhodím. Asi víte, že je to proto, aby neubíral benefit ostatním hráčům.

   <p>Z vzorce aktivity bude pro mě varovné číslo pod -0.5. Vidíte že podle příkladu je i hráč s celkem velkým prodělkem v pohodě. Co se týče mojeho hodnocení. Kdo je pod nulou, tak nic moc, ale nevyhodím ho.

   <h4>OSTATNÍ</h4>
